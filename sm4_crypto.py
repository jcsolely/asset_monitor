# ========== 密钥管理 ==========
_salt = None

def set_salt(salt):
    """设置应用密钥（必须在加密前调用）"""
    global _salt
    _salt = salt

def _get_key():
    """获取SM4密钥（16字节）"""
    if _salt:
        return hashlib.md5(_salt.encode()).digest()
    return secrets.token_bytes(16)


# ========== SM4 核心算法 ==========
def _rotl32(x, n):
    """32位循环左移"""
    return ((x << n) | (x >> (32 - n))) & 0xFFFFFFFF

def _tau(a):
    """非线性变换 τ（S-盒替换）"""
    return (
        (S_BOX[(a >> 24) & 0xFF] << 24) |
        (S_BOX[(a >> 16) & 0xFF] << 16) |
        (S_BOX[(a >> 8) & 0xFF] << 8) |
        S_BOX[a & 0xFF]
    )

def _l(b):
    """线性变换 L"""
    return b ^ _rotl32(b, 2) ^ _rotl32(b, 10) ^ _rotl32(b, 18) ^ _rotl32(b, 24)

def _l_prime(b):
    """线性变换 L'（密钥扩展用）"""
    return b ^ _rotl32(b, 13) ^ _rotl32(b, 23)

def _t(a):
    """合成变换 T = τ ∘ L"""
    return _l(_tau(a))

def _t_prime(a):
    """合成变换 T' = τ ∘ L'"""
    return _l_prime(_tau(a))

def _key_expansion(key):
    """密钥扩展，生成32轮密钥"""
    mk = [int.from_bytes(key[i*4:(i+1)*4], 'big') for i in range(4)]
    k = [mk[i] ^ FK[i] for i in range(4)]
    rk = []
    for i in range(32):
        k_val = k[i] ^ _t_prime(k[i+1] ^ k[i+2] ^ k[i+3] ^ CK[i])
        k.append(k_val)
        rk.append(k_val)
    return rk

def _encrypt_block(block, rk):
    """加密单个16字节块"""
    x = [int.from_bytes(block[i*4:(i+1)*4], 'big') for i in range(4)]
    for i in range(32):
        tmp = x[0] ^ _t(x[1] ^ x[2] ^ x[3] ^ rk[i])
        x = [x[1], x[2], x[3], tmp]
    # 反序输出
    return b''.join(x[3-i].to_bytes(4, 'big') for i in range(4))

def _decrypt_block(block, rk):
    """解密单个16字节块（密钥反序）"""
    return _encrypt_block(block, rk[::-1])


# ========== PKCS7 填充 ==========
def _pkcs7_pad(data):
    """PKCS7 填充到16字节对齐"""
    pad_len = 16 - (len(data) % 16)
    return data + bytes([pad_len] * pad_len)

def _pkcs7_unpad(data):
    """移除 PKCS7 填充"""
    if not data:
        return data
    pad_len = data[-1]
    if pad_len < 1 or pad_len > 16:
        return data
    if data[-pad_len:] != bytes([pad_len] * pad_len):
        return data
    return data[:-pad_len]


# ========== CBC 模式 ==========
def _xor_bytes(a, b):
    """两个等长字节串异或"""
    return bytes(x ^ y for x, y in zip(a, b))


# ========== 公开接口 ==========
def sm4_encrypt(plaintext):
    """SM4-CBC 加密，返回 hex 字符串（含 IV 前缀）"""
    if not plaintext:
        return ''
    if isinstance(plaintext, str):
        plaintext = plaintext.encode('utf-8')
    
    key = _get_key()
    rk = _key_expansion(key)
    iv = secrets.token_bytes(16)
    
    padded = _pkcs7_pad(plaintext)
    blocks = [padded[i:i+16] for i in range(0, len(padded), 16)]
    
    ciphertext = iv
    prev = iv
    for block in blocks:
        encrypted = _encrypt_block(_xor_bytes(block, prev), rk)
        ciphertext += encrypted
        prev = encrypted
    
    return ciphertext.hex()

def sm4_decrypt(hex_str):
    """SM4-CBC 解密，输入 hex 字符串（含 IV 前缀），返回明文字符串"""
    if not hex_str:
        return ''
    
    data = bytes.fromhex(hex_str)
    if len(data) < 32:  # 至少 IV(16) + 一个块(16)
        return ''
    
    key = _get_key()
    rk = _key_expansion(key)
    
    iv = data[:16]
    blocks = [data[i:i+16] for i in range(16, len(data), 16)]
    
    plaintext = b''
    prev = iv
    for block in blocks:
        decrypted = _decrypt_block(block, rk)
        plaintext += _xor_bytes(decrypted, prev)
        prev = block
    
    result = _pkcs7_unpad(plaintext)
    try:
        return result.decode('utf-8')
    except UnicodeDecodeError:
        return result.hex()
